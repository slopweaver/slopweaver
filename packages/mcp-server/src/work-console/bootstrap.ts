export function runBootstrapWorkConsole(
  args: RunBootstrapArgs,
): ResultAsync<BootstrapResult, BootstrapWorkConsoleFailedError> {
  const branch = args.branch ?? DEFAULT_WORK_CONSOLE_BRANCH;
  const config = resolveWorkConsoleConfig({
    cwd: args.cwd,
    branch,
  });
  const branchPromise = ensureWorkConsoleBranch({
    config,
    ...(args.gitRunner !== undefined && { runner: args.gitRunner }),
    allowSwitchWithUncommitted: args.allowSwitchWithUncommitted ?? false,
  });
  return branchPromise
    .mapErr<BootstrapWorkConsoleFailedError>((e) => failed(e.message))
    .andThen((branchResult) => {
      const branchAction: BootstrapBranchAction = branchResult.action;
      const consoleDir = `${config.cwd}/${DEFAULT_CONSOLE_REL_DIR}`;
      return runScaffold({ consoleDir, writers: args.writers }).andThen((filesCreated) =>
        runMemoryFile({ cwd: config.cwd, writers: args.writers }).andThen((memoryOutcome) =>
          runSlashCommands({ cwd: config.cwd, writers: args.writers }).map((slashCommandsCreated) => ({
            branch,
            branchAction,
            consoleDir,
            filesCreated,
            memoryFileCreated: memoryOutcome.memoryFileCreated,
            claudeMdImportAdded: memoryOutcome.claudeMdImportAdded,
            slashCommandsCreated,
          })),
        ),
      );
    });
}

function runSlashCommands(args: {
  cwd: string;
  writers: Writers;
}): ResultAsync<ReadonlyArray<string>, BootstrapWorkConsoleFailedError> {
  const promises = SLASH_COMMAND_SHIMS.map(async (shim): Promise<string | null> => {
    const relPath = `${SLASH_COMMAND_REL_DIR}/${shim.name}.md`;
    const absPath = `${args.cwd}/${relPath}`;
    const exists = await args.writers.fileExists(absPath);
    if (exists) return null;
    await args.writers.writeFile(absPath, renderSlashCommandShim(shim.name, shim.description));
    return relPath;
  });
  return wrapAll(promises);
}

function runScaffold(args: {
  consoleDir: string;
  writers: Writers;
}): ResultAsync<ReadonlyArray<string>, BootstrapWorkConsoleFailedError> {
  const promises = SCAFFOLD_FILES.map(async (entry): Promise<string | null> => {
    const absPath = `${args.consoleDir}/${entry.relPath}`;
    const exists = await args.writers.fileExists(absPath);
    if (exists) return null;
    await args.writers.writeFile(absPath, entry.seedContent);
    return entry.relPath;
  });
  return wrapAll(promises);
}

function runMemoryFile(args: {
  cwd: string;
  writers: Writers;
}): ResultAsync<{ memoryFileCreated: boolean; claudeMdImportAdded: boolean }, BootstrapWorkConsoleFailedError> {
  const memoryAbs = `${args.cwd}/${SLOPWEAVER_MEMORY_REL_PATH}`;
  const claudeMdAbs = `${args.cwd}/${PROJECT_CLAUDE_MD_REL_PATH}`;
  const importLine = `@${SLOPWEAVER_MEMORY_REL_PATH}`;
  const importStanza = `\n${importLine}\n`;

  const memoryPromise = (async (): Promise<boolean> => {
    const exists = await args.writers.fileExists(memoryAbs);
    if (exists) return false;
    await args.writers.writeFile(memoryAbs, SLOPWEAVER_MEMORY_CONTENT);
    return true;
  })();

  const claudeMdPromise = (async (): Promise<boolean> => {
    const existing = await args.writers.readFile(claudeMdAbs);
    if (existing === null) {
      const seed = `# Claude Code memory\n${importStanza}`;
      await args.writers.writeFile(claudeMdAbs, seed);
      return true;
    }
    if (existing.includes(importLine)) {
      return false;
    }
    const next = existing.endsWith('\n') ? `${existing}${importStanza}` : `${existing}\n${importStanza}`;
    await args.writers.writeFile(claudeMdAbs, next);
    return true;
  })();

  return ResultAsync.fromPromise<
    { memoryFileCreated: boolean; claudeMdImportAdded: boolean },
    BootstrapWorkConsoleFailedError
  >(
    Promise.all([memoryPromise, claudeMdPromise]).then(([memoryFileCreated, claudeMdImportAdded]) => ({
      memoryFileCreated,
      claudeMdImportAdded,
    })),
    (e) => failed(e instanceof Error ? e.message : String(e)),
  );
}

function wrapAll<T>(
  promises: ReadonlyArray<Promise<T | null>>,
): ResultAsync<ReadonlyArray<T>, BootstrapWorkConsoleFailedError> {
  return ResultAsync.fromPromise<ReadonlyArray<T | null>, BootstrapWorkConsoleFailedError>(Promise.all(promises), (e) =>
    failed(e instanceof Error ? e.message : String(e)),
  ).andThen<ReadonlyArray<T>, BootstrapWorkConsoleFailedError>((results) =>
    okAsync(results.filter((r): r is T => r !== null)),
  );
}

