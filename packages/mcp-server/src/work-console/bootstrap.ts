function renderSlashCommandShim(name: string, description: string): string {
  return `---
description: ${description}
---

Fetch and run the SlopWeaver MCP server's \`${name}\` prompt now. Use
the MCP \`prompts/get\` request with \`name: "${name}"\` against the
\`slopweaver\` server, and follow the returned prompt body exactly.

Forward any arguments the user typed after \`/${name}\` to the MCP
prompt's argument map (see its argument schema). When there are no
arguments, send an empty arguments object.

Equivalent invocation: \`/mcp__slopweaver__${name}\`. This shim exists
so the bare \`/${name}\` name appears in the slash-command menu.
`;
}

interface BootstrapWorkConsoleFailedError extends BaseError {
  readonly code: 'BOOTSTRAP_WORK_CONSOLE_FAILED';
  readonly detail: string;
}

const failed = (detail: string): BootstrapWorkConsoleFailedError => ({
  code: 'BOOTSTRAP_WORK_CONSOLE_FAILED',
  message: `Work-console bootstrap failed: ${detail}`,
  detail,
});

