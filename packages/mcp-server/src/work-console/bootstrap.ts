type WriteResult = {
  bytesWritten: number;
  created: boolean;
};

type Writers = {
  readonly fileExists: (absPath: string) => Promise<boolean>;
  readonly readFile: (absPath: string) => Promise<string | null>;
  readonly writeFile: (absPath: string, content: string) => Promise<WriteResult>;
  readonly mkdir: (absPath: string) => Promise<void>;
};

export type BootstrapBranchAction = 'no_git_repo' | 'already_on_branch' | 'switched' | 'created_and_switched';

export type BootstrapResult = {
  branch: string;
  branchAction: BootstrapBranchAction;
  consoleDir: string;
  filesCreated: ReadonlyArray<string>;
  memoryFileCreated: boolean;
  claudeMdImportAdded: boolean;
  /** Relative paths of slash-command shims created under .claude/commands/. */
  slashCommandsCreated: ReadonlyArray<string>;
};

