/**
 * Baseline scoreboard runner. Shells the REAL `slopweaver ask --json` for every golden case, REPS times
 * (default 3), scores each result with the pure deterministic scorer, aggregates across reps, and writes
 * a metrics-ONLY scoreboard to docs/eval-baseline.md. Metrics only (no answer text) ⇒ the committed
 * scoreboard is always safe for the public repo.
 *
 * Usage: yarn eval:scoreboard [reps]
 *
 * Retrieval recall is deterministic (run once conceptually, verified stable across reps); answer recall
 * and citation precision are stochastic, so they are summarised as median + [min–max] over the reps.
 */
import { execFileSync } from 'node:child_process'
import { writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { GOLDEN_CASES, parseScorableAnswer, scoreGrounding, type GoldenCase } from '../src/eval/scorer.js'
import { aggregateCase, renderScoreboard, type CaseAggregate } from '../src/eval/scoreboard.js'
import type { GroundingScore } from '../src/eval/scorer.js'

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const reps = Math.max(1, Number.parseInt(process.argv[2] ?? '3', 10) || 3)

/** Run one case once through the real CLI and score it, or null if the run/output was unusable. */
function scoreOnce({ golden }: { golden: GoldenCase }): GroundingScore | null {
  const stdout = execFileSync('yarn', ['slopweaver', 'ask', golden.question, '--json'], {
    cwd: repoRoot,
    encoding: 'utf8',
    maxBuffer: 16 * 1024 * 1024,
  })
  const answer = parseScorableAnswer({ value: JSON.parse(stdout) })
  return answer === null ? null : scoreGrounding({ expectedGrounding: golden.expectedGrounding, answer })
}

const rows: CaseAggregate[] = []
for (const golden of GOLDEN_CASES) {
  const scores: GroundingScore[] = []
  for (let rep = 0; rep < reps; rep += 1) {
    process.stderr.write(`scoring [${golden.kind}] "${golden.question}" — rep ${String(rep + 1)}/${String(reps)}\n`)
    const score = scoreOnce({ golden })
    if (score !== null) {
      scores.push(score)
    }
  }
  if (scores.length > 0) {
    rows.push(aggregateCase({ question: golden.question, kind: golden.kind, scores }))
  }
}

const body = [
  '# Slopweaver eval — baseline scoreboard',
  '',
  '> Generated by `yarn eval:scoreboard` against the local Slopweaver corpus (the plugin\'s own public',
  '> GitHub history). Metrics only — no answer text — so this snapshot is safe to commit. Retrieval',
  '> recall@k is the deterministic gate; answer recall + citation precision are advisory and stochastic.',
  '',
  renderScoreboard({ rows }),
  '',
].join('\n')

writeFileSync(resolve(repoRoot, 'docs/eval-baseline.md'), body, 'utf8')
process.stderr.write(`\nwrote docs/eval-baseline.md (${String(rows.length)} cases, ${String(reps)} reps)\n`)
